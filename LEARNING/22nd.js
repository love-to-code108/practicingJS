
// console.table() can take both arrays and objects ;
// console.table() creates two columbs namely (index) and values
// console.table() will always follow an ascending order for the elements in the index part that is for the keys they will be ordered in ascending order , 
// if its an array it will be in the order of its index number , for eg;

// array example
console.table(["h","e","l","l","o"]);

// object example
console.table({"99":1344,"hello world":12,"55":69});

// after you compile the program as you can see 55 comes first and then 99 because they are in ascending order